//program for accessing variable
#include <stdio.h>
int main()
 {
    int a = 5;
    float b = 3.14;
    char c = 'X';

    printf("The value of integer variable a is: %d\n", a);
    printf("The value of float variable b is: %f\n", b);
    printf("The value of character variable c is: %c\n", c);
    return 0;
}
